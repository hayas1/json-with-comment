use serde::de;

use crate::value::JsoncValue;

use super::number::NumberDeserializer;

pub struct ValueDeserializer<I, F> {
    value: JsoncValue<I, F>,
}
impl<I, F> ValueDeserializer<I, F> {
    pub fn new(value: JsoncValue<I, F>) -> Self {
        Self { value }
    }
}

